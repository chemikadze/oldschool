int N;
int a[100][2];

int main(int argc, char* argv[])
{
  int i, j;
  scanf("%d", &N);
  a[0][0]=1;
  a[0][1]=1;
  for (i=1; i<N; i++)
  {
    a[i][0]=a[i-1][0]+a[i-1][1];
    a[i][1]=a[i-1][0];
  }
  printf("%d", a[N-1][0]+a[N-1][1]);
  #ifdef _DEBUG
  getch();
  #endif
  return 0;
}
